import assert from 'node:assert/strict';
import fs from 'node:fs';
const migration = fs.readFileSync('supabase/migrations/20260702050000_competition_template_candidates.sql','utf8');
const component = fs.readFileSync('app/components/CompetitionTemplateSuggestions.tsx','utf8');
const newSession = fs.readFileSync('app/sessions/new/page.tsx','utf8');
const newResult = fs.readFileSync('app/results/new/page.tsx','utf8');
const scorecardImport = fs.readFileSync('app/sessions/[id]/scorecard-import/page.tsx','utf8');
assert.match(migration,/find_competition_template_candidates/, 'candidate RPC exists');
assert.match(migration,/t\.visibility = 'searchable'/, 'private and link templates are never candidates');
assert.match(migration,/t\.withdrawn_at is null/, 'withdrawn templates are never candidates');
assert.match(migration,/t\.discipline = i\.q_discipline/, 'same discipline is required');
assert.match(migration,/fitasc sporting/, 'FITASC Sporting is excluded');
assert.match(migration,/public\.has_approved_access\(auth\.uid\(\)\)/, 'approved access is required');
assert.match(migration,/t\.competition_date = p_competition_date then 40/, 'same date scores highly');
assert.match(migration,/t\.normalized_name = i\.q_name then 30/, 'same normalized name scores highly');
assert.match(migration,/t\.normalized_shooting_ground = i\.q_ground then 15/, 'same normalized ground scores');
assert.match(migration,/t\.target_count = p_target_count then 15/, 'same target count scores');
assert.match(migration,/f\.score >= 45/, 'weak candidates under threshold are hidden');
assert.match(migration,/order by f\.score desc, f\.is_complete desc, f\.updated_at desc, f\.template_version desc, f\.id asc/, 'stable ranking and tie-breakers are explicit');
assert.match(migration,/least\(coalesce\(p_limit, 5\), 8\)/, 'max result limit is enforced');
const returnBlock = migration.slice(migration.indexOf('returns table (', migration.indexOf('find_competition_template_candidates')), migration.indexOf(')\nlanguage sql security definer', migration.indexOf('find_competition_template_candidates')));
assert.doesNotMatch(returnBlock,/owner_user_id|source_session_id|template_payload|email/i, 'private fields are not returned');
assert.match(migration,/match_reasons text\[\]/, 'match reasons are returned');
assert.match(component,/Could not check for shared setups\. You can continue without one\./, 'UI does not block creation on RPC failure');
assert.match(component,/Not the same competition/, 'one candidate can be dismissed');
assert.match(component,/Dismiss all suggestions/, 'all suggestions can be dismissed');
assert.match(component,/href=\{`\/competition-templates\/\$\{candidate\.id\}`\}/, 'preview uses existing template preview page/RPC');
assert.match(component,/copy_competition_template_to_new_session/, 'template setup is copied only into a new session');
assert.match(component,/Your competition details will be kept/, 'metadata conflict rule is shown before use');
assert.match(component,/p_name: metadata\.name/, 'user name is kept');
assert.match(component,/p_competition_date: metadata\.competitionDate/, 'user date is kept');
assert.match(component,/p_shooting_ground: metadata\.shootingGround/, 'user ground is kept');
assert.match(newSession,/CompetitionTemplateSuggestions/, 'normal competition creation shows suggestions');
assert.match(newResult,/CompetitionTemplateSuggestions/, 'result-only creation shows suggestions');
assert.match(scorecardImport,/CompetitionTemplateSuggestions/, 'scorecard import review shows suggestions when session metadata exists');
console.log('competition template candidate tests passed');
